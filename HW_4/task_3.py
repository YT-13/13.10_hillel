# Перевірка пароля:
# У користувача програма запитуює пароль. Якщо пароль не відповідає збереженим паролям то відмовити йому у доступі.
# Інакше повідомити користувачу що пароль прийнято.

password = input('Введіть пароль, який буде використовуватись для вашого акаунту >>> ')
audit_password = input('Для ввходу на сайт введіть ваш пароль >>> ')
if password == audit_password:
    print('Ваш пароль прийнято!')
else:
    print('Невірний пароль, у доступі відмовлено!')